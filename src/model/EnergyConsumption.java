package model;

public class EnergyConsumption {
    private int gazConsumption;
    private int electricityConsumption;

    public int getGazConsumption() {
        return gazConsumption;
    }

    public void setGazConsumption(int gazConsumption) {
        this.gazConsumption = gazConsumption;
    }

    public int getElectricityConsumption() {
        return electricityConsumption;
    }

    public void setElectricityConsumption(int electricityConsumption) {
        this.electricityConsumption = electricityConsumption;
    }
}
